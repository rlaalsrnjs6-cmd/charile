public class InspectionDTO {

    private int empno;
    private String regist_time;
    private String ph_num;
    private String washed;

    public int getEmpno() { return empno; }
    public void setEmpno(int empno) { this.empno = empno; }

    public String getRegist_time() { return regist_time; }
    public void setRegist_time(String regist_time) { this.regist_time = regist_time; }

    public String getPh_num() { return ph_num; }
    public void setPh_num(String ph_num) { this.ph_num = ph_num; }

    public String getWashed() { return washed; }
    public void setWashed(String washed) { this.washed = washed; }
}